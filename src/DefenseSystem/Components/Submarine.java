    @Override
    public void setPrivateMsg(int index2, String msgPri) {
        this.index2 = index2;
        this.msgPri = msgPri;
        if (this.index2 == this.id) {
            jTextArea2.append(msgPri);
        }
    }

    @Override
    public void getInfo(int index3) {
        this.index3 = index3;
        if (id == index3) {
            mainControllerInterface.setInfo((int) jSpinner1.getValue(), (int) jSpinner2.getValue(), jSlider3.getValue(), getTitle() + " - Oxygen Capacity : " + jSlider2.getValue());
        }
    }
    @Override
    public void setAmmoAmount(int index, int amount) {
        if(id==index){
         jSpinner2.setValue(amount);
        }
    }
